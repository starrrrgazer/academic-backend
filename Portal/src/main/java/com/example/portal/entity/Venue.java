package com.example.portal.entity;

public class Venue {
    private String id;
    private String raw;

    public void setId(String id) {
        this.id = id;
    }

    public void setRaw(String raw) {
        this.raw = raw;
    }

    public String getId() {
        return id;
    }

    public String getRaw() {
        return raw;
    }
}
